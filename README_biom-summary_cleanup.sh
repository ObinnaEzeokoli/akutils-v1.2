I made this for me because some of my scripts leave me with biom table summaries
with dual extensions (.biom.summary) because I don't know how to extract the
directory structure from a defined variable.  It should work for any file with 
dual extension where the first extension is biom (*.biom.* will become *.summary).

To use, place it in your path and call the script by its name.

biom-summary_cleanup.sh
